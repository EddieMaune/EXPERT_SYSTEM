#include "expert_system.h"

int      is_fact(char *s)
{
	if ((ft_isupper(*s) && ft_strlen(s) == 1))
		return (1);
	else if (*s == '!' && ft_isupper(*(s + 1)))
		return (1);
	else if ((*s == '!' || ft_isupper(*s)) && (*(s + 1) == ')' || *(s + 2) == ')'))
		return (1);
	return (0);
}
