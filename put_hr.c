#include "expert_system.h"

void		put_hr(char *str, char c)
{
	int		i;

	i = 0;
	while (str[i])
	{
		ft_putchar(c);
		i++;
	}
	ft_putchar(c);
	ft_putchar(c);
	ft_putchar('\n');
}
